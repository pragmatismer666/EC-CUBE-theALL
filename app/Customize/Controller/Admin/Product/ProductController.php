     * @param \Eccube\Entity\Product $Product
     * @param \Eccube\Entity\Category $Category
     * @param integer $count
     *
     * @return \Eccube\Entity\ProductCategory
     */
    protected function createProductCategory($Product, $Category, $count)
    {
        $ProductCategory = new ProductCategory();
        $ProductCategory->setProduct($Product);
        $ProductCategory->setProductId($Product->getId());
        $ProductCategory->setCategory($Category);
        $ProductCategory->setCategoryId($Category->getId());

        return $ProductCategory;
    }

    public function checkIfProductRelatedWithOrders($Product)
    {
        $orderItem_repository = $this->entityManager->getRepository(OrderItem::class);
        $relatedItem = $orderItem_repository->findOneBy(['Product' => $Product]);
        if (empty($relatedItem)) return null;
        return $relatedItem->getOrder();
    }
    public function checkIfProductRelatedClassName($Product)
    {
        $product_class_repo = $this->entityManager->getRepository(ProductClass::class);
        $ProductClasses = $product_class_repo->findBy(['Product' => $Product]);
        return !empty($ProductClasses);
    }
